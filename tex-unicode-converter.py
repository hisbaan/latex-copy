def convert_replace(tex: str) -> str:
    """Replace tex commands with their unicode symbols"""
    # quantifiers
    tex = tex.replace('\\forall', '∀')
    tex = tex.replace('\\exists', '∃')

    # comparison operators
    tex = tex.replace('\\geq', '≥')
    tex = tex.replace('\\leq', '≥')
    tex = tex.replace('\\neq', '≠')
    tex = tex.replace('\\equiv', '≡')
    tex = tex.replace('\\nequiv', '≢')

    # logical operators
    # in, not in, contains, does not contain
    tex = tex.replace('\\in', '∈')
    tex = tex.replace('\\lnot', '¬')
    tex = tex.replace('\\land', '∧')
    tex = tex.replace('\\wedge', '∧')
    tex = tex.replace('\\lor', '∨')
    tex = tex.replace('\\vee', '∨')
    # tex = tex.replace('\\contains')

    # have these from the longest to the shortest so
    # there's no overlap
    tex = mathcal(tex)
    tex = mathbr(tex)
    tex = mathbfit(tex)
    tex = superscript(tex)
    tex = subscript(tex)
    tex = greek_letters(tex)
    tex = symbols(tex)
    tex = blackboard(tex)
    tex = normal_letters(tex)
    tex = personal_aliases(tex)

    return tex

