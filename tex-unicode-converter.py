def convert_replace(tex: str) -> str:
    """Replace tex commands with their unicode symbols"""
    # quantifiers
    tex = tex.replace('\\forall', '‚àÄ')
    tex = tex.replace('\\exists', '‚àÉ')

    # comparison operators
    tex = tex.replace('\\geq', '‚â•')
    tex = tex.replace('\\leq', '‚â•')
    tex = tex.replace('\\neq', '‚â†')
    tex = tex.replace('\\equiv', '‚â°')
    tex = tex.replace('\\nequiv', '‚â¢')

    # logical operators
    # in, not in, contains, does not contain
    tex = tex.replace('\\in', '‚àà')
    tex = tex.replace('\\lnot', '¬¨')
    tex = tex.replace('\\land', '‚àß')
    tex = tex.replace('\\wedge', '‚àß')
    tex = tex.replace('\\lor', '‚à®')
    tex = tex.replace('\\vee', '‚à®')
    # tex = tex.replace('\\contains')

    # TODO have these from the longest to the shortest so
    # there's no overlap
    tex = mathcal(tex)
    tex = mathbr(tex)
    tex = mathbfit(tex)
    tex = superscript(tex)
    tex = subscript(tex)
    tex = greek_letters(tex)
    tex = blackboard(tex)
    tex = normal_letters(tex)

    return tex


